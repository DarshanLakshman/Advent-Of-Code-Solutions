def find_repeating_pattern(lst):
    for i in range(len(lst)):
        for j in range(1,i):
            pattern = lst[i:i+j+1]
            if pattern[i:i+j+1] == pattern[i+j:i*(i-j+1)+j+1]:
                return lst[i:i+j+1]
    return False

